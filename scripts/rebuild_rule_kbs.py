def write_targets() -> dict[str, int]:
    KB_DIR.mkdir(parents=True, exist_ok=True)
    contents = {
        COMPLIANCE_FILE: compliance_markdown(),
        PHYSICS_FILE: physics_markdown(),
        SOP_FILE: sop_markdown(),
    }
    sizes: dict[str, int] = {}
    for filename, content in contents.items():
        path = KB_DIR / filename
        path.write_text(content.rstrip() + "\n", encoding="utf-8")
        sizes[filename] = path.stat().st_size
    return sizes


def _load_split_by_headers():
    if str(PROJECT_ROOT) not in sys.path:
        sys.path.insert(0, str(PROJECT_ROOT))
    from harness.vector_store import split_by_headers

    return split_by_headers


def count_ids(text: str, prefix: str) -> int:
    return len(set(re.findall(rf"\b{re.escape(prefix)}-[A-Z]+-[0-9]{{3}}\b", text)))


def count_sop_ids(text: str) -> int:
    return len(set(re.findall(r"\bSOP-[A-Z]+-[A-Z0-9]+\b", text)))


def quality_check() -> dict[str, object]:
    split_by_headers = _load_split_by_headers()
    checks: list[dict[str, object]] = []
    for filename in TARGET_FILES:
        path = KB_DIR / filename
        text = path.read_text(encoding="utf-8") if path.exists() else ""
        chunks = split_by_headers(text, max_chunk_size=500, overlap=80)
        has_rule_or_sop = ("rule_id" in text) or ("sop_id" in text)
        if filename == COMPLIANCE_FILE:
            min_ids = 20
            id_count = count_ids(text, "COM")
        elif filename == PHYSICS_FILE:
            min_ids = 20
            id_count = count_ids(text, "PHY")
        else:
            min_ids = 10
            id_count = count_sop_ids(text)
        checks.append(
            {
                "file": filename,
                "exists": path.exists(),
                "size": len(text.encode("utf-8")),
                "non_empty": bool(text.strip()),
                "no_incremental_update_block": "增量更新" not in text,
                "has_source_sections": "数据来源" in text and "规则来源" in text,
                "has_rule_or_sop_id": has_rule_or_sop,
                "id_count": id_count,
                "id_count_ok": id_count >= min_ids,
                "no_pending_placeholder": "待填写" not in text,
                "split_chunk_count": len(chunks),
                "split_by_headers_ok": len(chunks) >= 10 and all(chunk["text"].strip() for chunk in chunks),
            }
        )

    all_ok = all(
        item["exists"]
        and item["non_empty"]
        and item["no_incremental_update_block"]
        and item["has_source_sections"]
        and item["has_rule_or_sop_id"]
        and item["id_count_ok"]
        and item["no_pending_placeholder"]
        and item["split_by_headers_ok"]
        for item in checks
    )
    return {
        "generated_at": datetime.now().isoformat(timespec="seconds"),
        "build_date": BUILD_DATE,
        "files": checks,
        "rule_counts": {
            "compliance": len(COMPLIANCE_RULES) + len(COMPLIANCE_ACTION_RULES),
            "physics": len(PHYSICS_RULES),
            "sop": len(SOP_RULES),
            "total": len(COMPLIANCE_RULES) + len(COMPLIANCE_ACTION_RULES) + len(PHYSICS_RULES) + len(SOP_RULES),
        },
        "validation_anchors": [
            "COM-RED-018",
            "COM-RED-019",
            "PHY-GEN-002",
            "PHY-GEN-003",
            "PHY-TS-006",
            "SOP-ROUTE-RED",
            "SOP-CHECK-002",
            "SOP-CLOSE-001",
        ],
        "all_ok": all_ok,
    }


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="Only run quality checks; do not rewrite Markdown.")
    parser.add_argument("--report-json", default=str(RUN_REPORT), help="Path for the JSON run report.")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    sizes: dict[str, int] = {}
    if not args.check:
        sizes = write_targets()
    summary = quality_check()
    summary["written_sizes"] = sizes
    report_path = Path(args.report_json)
    if not report_path.is_absolute():
        report_path = PROJECT_ROOT / report_path
    report_path.parent.mkdir(parents=True, exist_ok=True)
    report_path.write_text(json.dumps(summary, ensure_ascii=False, indent=2), encoding="utf-8")
    print(json.dumps(summary, ensure_ascii=False, indent=2))
    return 0 if summary["all_ok"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
